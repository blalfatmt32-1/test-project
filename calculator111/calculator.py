num1 = int(input("Enter a first number: "))
num2 = int(input("Enter a second number: "))

operator = input("Enter a operator: +, -, *, /: ")

if operator == "+":
    print(num1 + num2)
elif operator == "_":
    print(num1 - num2)
elif operator == "*":
    print(num1 * num2)
elif operator == "/":
    print(num1 / num2)
